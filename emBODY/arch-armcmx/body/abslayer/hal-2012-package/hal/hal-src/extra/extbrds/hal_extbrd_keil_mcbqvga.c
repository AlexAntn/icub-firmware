extern hal_result_t hal_extbrd_keil_mcbqvga_init(const hal_extbrd_keil_mcbqvga_cfg_t *cfg)
{
//    hal_result_t res = hal_res_NOK_generic; // dont remove ...
     
    if(NULL == cfg)
    {
        cfg  = &hal_extbrd_keil_mcbqvga_cfg_default;
    }
    
    if(hal_true == s_hal_extbrd_keil_mcbqvga_initted_is())
    {
        return(hal_res_OK);
    }    

    if(hal_res_OK != s_hal_extbrd_keil_mcbqvga_hw_init(cfg, &s_hal_extbrd_keil_mcbqvga_info[0]))
    {
        return(hal_res_NOK_generic);
    }
    
    hal_extbrd_keil_mcbqvga_settextproperties(hal_extbrd_keil_mcbqvga_fontsize_6x8, HAL_EXTBRD_KEIL_MCBQVGA_COLBLACK, HAL_EXTBRD_KEIL_MCBQVGA_COLWHITE);   
    
    s_hal_extbrd_keil_mcbqvga_initted_set();

    return(hal_res_OK);
}


extern hal_result_t hal_extbrd_keil_mcbqvga_clear(hal_extbrd_keil_mcbqvga_color_t color)
{
    hal_result_t res = hal_res_NOK_generic; 
    hal_spi_port_t spiport = s_hal_extbrd_keil_mcbqvga_info[0].cfg.spiport;

    
    if(hal_false == s_hal_extbrd_keil_mcbqvga_initted_is())
    {
        return(hal_res_NOK_generic);
    }
       
    // ok do what is required ...
    
    uint32_t i;
    
    s_hal_extbrd_keil_mcbqvga_windowmax();
    
    s_hal_extbrd_keil_mcbqvga_reg_write(0x20, 0);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x21, 0);
    s_hal_extbrd_keil_mcbqvga_cmd_write(0x22);
    
    s_hal_extbrd_keil_mcbqvga_dat_start();
    for(i=0; i<(HAL_EXTBRD_KEIL_MCBQVGA_WIDTH*HAL_EXTBRD_KEIL_MCBQVGA_HEIGHT); i++)
    {
        s_hal_extbrd_keil_mcbqvga_dat_onlywrite(color);
    }   
    s_hal_extbrd_keil_mcbqvga_dat_stop();    
   
    return(hal_res_OK);
}

extern hal_result_t hal_extbrd_keil_mcbqvga_settextproperties(hal_extbrd_keil_mcbqvga_fontsize_t fontsize, hal_extbrd_keil_mcbqvga_color_t backcolor, hal_extbrd_keil_mcbqvga_color_t textcolor)
{
    hal_result_t res = hal_res_NOK_generic; 
    hal_spi_port_t spiport = s_hal_extbrd_keil_mcbqvga_info[0].cfg.spiport;

    
    if(hal_false == s_hal_extbrd_keil_mcbqvga_initted_is())
    {
        return(hal_res_NOK_generic);
    }
    
    // ok do what is required ...    
    s_hal_extbrd_keil_mcbqvga_textcolors[HAL_EXTBRD_KEIL_MCBQVGA_POSCOLBACK]  = backcolor;
    s_hal_extbrd_keil_mcbqvga_textcolors[HAL_EXTBRD_KEIL_MCBQVGA_POSCOLTEXT] = textcolor;
    

    if(hal_extbrd_keil_mcbqvga_fontsize_16x24 == fontsize)
    {
        s_hal_extbrd_keil_mcbqvga_info[0].font  = 0;
        s_hal_extbrd_keil_mcbqvga_thefonts.w    = 16;
        s_hal_extbrd_keil_mcbqvga_thefonts.h    = 24;
        s_hal_extbrd_keil_mcbqvga_thefonts.s    = 2;
        s_hal_extbrd_keil_mcbqvga_thefonts.data = hal_extbrd_keil_mcbqvga_fonts_16x24;
    }
    else if(hal_extbrd_keil_mcbqvga_fontsize_6x8 == fontsize)
    {
        s_hal_extbrd_keil_mcbqvga_info[0].font  = 1;
        s_hal_extbrd_keil_mcbqvga_thefonts.w    = 6;
        s_hal_extbrd_keil_mcbqvga_thefonts.h    = 8;
        s_hal_extbrd_keil_mcbqvga_thefonts.s    = 1;
        s_hal_extbrd_keil_mcbqvga_thefonts.data = hal_extbrd_keil_mcbqvga_fonts_06x08;
    }
       

    return(hal_res_OK);
}

extern hal_result_t hal_extbrd_keil_mcbqvga_clearline(uint16_t line)
{   
    if(hal_false == s_hal_extbrd_keil_mcbqvga_initted_is())
    {
        return(hal_res_NOK_generic);
    }
    
    // ok do what is required ...    
    s_hal_extbrd_keil_mcbqvga_windowmax();   
    hal_extbrd_keil_mcbqvga_putstring(line, 0, "                    ");
       
   
    return(hal_res_OK);
}

extern hal_result_t hal_extbrd_keil_mcbqvga_putstring(uint16_t line,  uint16_t column, char* str)
{
    if(hal_false == s_hal_extbrd_keil_mcbqvga_initted_is())
    {
        return(hal_res_NOK_generic);
    }
    
    // ok do what is required ...    
    s_hal_extbrd_keil_mcbqvga_windowmax();   
    
    while(*str) 
    {
        hal_extbrd_keil_mcbqvga_putchar(line, column++, *str++);
    } 
   
    return(hal_res_OK);
}

extern hal_result_t hal_extbrd_keil_mcbqvga_putchar(uint16_t line, uint16_t column, char chr)
{
    if(hal_false == s_hal_extbrd_keil_mcbqvga_initted_is())
    {
        return(hal_res_NOK_generic);
    }
    
    // ok do what is required ...    
    
    chr -= 32;
    
    uint8_t* data = (uint8_t*) s_hal_extbrd_keil_mcbqvga_thefonts.data;
    data = &data[s_hal_extbrd_keil_mcbqvga_thefonts.s * chr * s_hal_extbrd_keil_mcbqvga_thefonts.h];


    //s_hal_extbrd_keil_mcbqvga_draw_char(column * s_hal_extbrd_keil_mcbqvga_thefonts.w, line * s_hal_extbrd_keil_mcbqvga_thefonts.h, (uint8_t*)&s_hal_extbrd_keil_mcbqvga_thefonts.data[chr * s_hal_extbrd_keil_mcbqvga_thefonts.h]);
    s_hal_extbrd_keil_mcbqvga_draw_char(column * s_hal_extbrd_keil_mcbqvga_thefonts.w, line * s_hal_extbrd_keil_mcbqvga_thefonts.h, data);
  
    return(hal_res_OK);
}


