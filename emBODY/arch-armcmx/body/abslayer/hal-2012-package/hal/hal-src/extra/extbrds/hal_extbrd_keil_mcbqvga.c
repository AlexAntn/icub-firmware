static hal_result_t s_hal_extbrd_keil_mcbqvga_hw_init(const hal_extbrd_keil_mcbqvga_cfg_t *cfg, hal_extbrd_keil_mcbqvga_info_t* info)
{
    hal_result_t res = hal_res_NOK_generic;   
    uint8_t data;
    hal_spi_port_t spiport = cfg->spiport;

#if     defined(HAL_EXTBRD_KEIL_MCBQVGA_SPI_USEACTIVITY_RAW08BIT)    
    static const hal_spi_cfg_t spicfg =
    {
        .ownership              = hal_spi_ownership_master,
        .dir                    = hal_spi_dir_txonly,
        .activity               = hal_spi_act_raw08bits,
        .speed                  = hal_spi_speed_18mbps,
        .sizeoftxframe          = 0,
        .sizeofrxframe          = 0,
        .onframetransm          = NULL,
        .onframereceiv          = NULL,
        .sizeoftxfifoofframes   = 0,
        .sizeofrxfifoofframes   = 0    
    };
#elif   defined(HAL_EXTBRD_KEIL_MCBQVGA_SPI_USEACTIVITY_SINGLEFRAME)
    static const hal_spi_cfg_t spicfg =
    {
        .ownership              = hal_spi_ownership_master,
        .dir                    = hal_spi_dir_txonly,
        .activity               = hal_spi_act_singleframe,
        .speed                  = hal_spi_speed_18mbps,
        .sizeoftxframe          = 1,
        .sizeofrxframe          = 1,
        .onframetransm          = NULL,
        .onframereceiv          = s_hal_extbrd_keil_mcbqvga_spiframe_receiv,
        .sizeoftxfifoofframes   = 4,
        .sizeofrxfifoofframes   = 4    
    };
#endif    
 
    // init spi    
    if(hal_res_OK != (res = hal_spi_init(spiport, &spicfg)))
    {
        return(res);
    }
    
    // put cs high   
    hal_gpio_configure(gpio_cs, NULL);
    hal_gpio_setval(gpio_cs.port, gpio_cs.pin, hal_gpio_valHIGH);
    
    hal_gpio_configure(gpio_STLD40D_enable, NULL);
    hal_gpio_setval(gpio_STLD40D_enable.port, gpio_STLD40D_enable.pin, hal_gpio_valHIGH);
    
    hal_sys_delay(50*1000);
    
    
    // config now ...
    
    uint16_t driverCode = s_hal_extbrd_keil_mcbqvga_reg_read(0x00);
    
    // following code is taken from keil's driver 
    
    /* Start Initial Sequence --------------------------------------------------*/
    s_hal_extbrd_keil_mcbqvga_reg_write(0x01, 0x0100);                 /* Set SS bit                         */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x02, 0x0700);                 /* Set 1 line inversion               */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x04, 0x0000);                 /* Resize register                    */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x08, 0x0207);                 /* 2 lines front, 7 back porch        */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x09, 0x0000);                 /* Set non-disp area refresh cyc ISC  */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x0A, 0x0000);                 /* FMARK function                     */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x0C, 0x0000);                 /* RGB interface setting              */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x0D, 0x0000);                 /* Frame marker Position              */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x0F, 0x0000);                 /* RGB interface polarity             */

    /* Power On sequence -------------------------------------------------------*/
    s_hal_extbrd_keil_mcbqvga_reg_write(0x10, 0x0000);                 /* Reset Power Control 1              */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x11, 0x0000);                 /* Reset Power Control 2              */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x12, 0x0000);                 /* Reset Power Control 3              */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x13, 0x0000);                 /* Reset Power Control 4              */
    hal_sys_delay(200*1000);                            /* Discharge cap power voltage (200ms)*/
    s_hal_extbrd_keil_mcbqvga_reg_write(0x10, 0x12B0);                 /* SAP, BT[3:0], AP, DSTB, SLP, STB   */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x11, 0x0007);                 /* DC1[2:0], DC0[2:0], VC[2:0]        */
    hal_sys_delay(50*1000);                             /* Delay 50 ms                        */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x12, 0x01BD);                 /* VREG1OUT voltage                   */
    hal_sys_delay(50*1000);                             /* Delay 50 ms                        */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x13, 0x1400);                 /* VDV[4:0] for VCOM amplitude        */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x29, 0x000E);                 /* VCM[4:0] for VCOMH                 */
    hal_sys_delay(50*1000);                             /* Delay 50 ms                        */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x20, 0x0000);                 /* GRAM horizontal Address            */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x21, 0x0000);                 /* GRAM Vertical Address              */

    
    /* Adjust the Gamma Curve --------------------------------------------------*/
    if(driverCode == 0x5408) 
    {           /* LCD with touch                     */
        s_hal_extbrd_keil_mcbqvga_reg_write(0x30, 0x0B0D);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x31, 0x1923);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x32, 0x1C26);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x33, 0x261C);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x34, 0x2419);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x35, 0x0D0B);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x36, 0x1006);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x37, 0x0610);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x38, 0x0706);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x39, 0x0304);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x3A, 0x0E05);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x3B, 0x0E01);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x3C, 0x010E);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x3D, 0x050E);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x3E, 0x0403);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x3F, 0x0607);
    }
    if(driverCode == 0xC990) 
    {           /* LCD without touch                  */
        s_hal_extbrd_keil_mcbqvga_reg_write(0x30, 0x0006);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x31, 0x0101);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x32, 0x0003);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x35, 0x0106);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x36, 0x0B02);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x37, 0x0302);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x38, 0x0707);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x39, 0x0007);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x3C, 0x0600);
        s_hal_extbrd_keil_mcbqvga_reg_write(0x3D, 0x020B);
    }

    /* Set GRAM area -----------------------------------------------------------*/
    s_hal_extbrd_keil_mcbqvga_reg_write(0x50, 0x0000);                 /* Horizontal GRAM Start Address      */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x51, (HAL_EXTBRD_KEIL_MCBQVGA_HEIGHT-1));             /* Horizontal GRAM End   Address      */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x52, 0x0000);                 /* Vertical   GRAM Start Address      */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x53, (HAL_EXTBRD_KEIL_MCBQVGA_WIDTH-1));              /* Vertical   GRAM End   Address      */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x60, 0xA700);                 /* Gate Scan Line                     */
    if(driverCode == 0x5408)             
    {   /* LCD with touch                     */
        s_hal_extbrd_keil_mcbqvga_reg_write(0x60, 0xA700);               /* Gate Scan Line                     */
    }
    if(driverCode == 0xC990) 
    {    /* LCD without touch                  */
        s_hal_extbrd_keil_mcbqvga_reg_write(0x60, 0x2700);               /* Gate Scan Line                     */
    }
    s_hal_extbrd_keil_mcbqvga_reg_write(0x61, 0x0001);                 /* NDL,VLE, REV                       */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x6A, 0x0000);                 /* Set scrolling line                 */

    /* Partial Display Control -------------------------------------------------*/
    s_hal_extbrd_keil_mcbqvga_reg_write(0x80, 0x0000);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x81, 0x0000);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x82, 0x0000);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x83, 0x0000);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x84, 0x0000);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x85, 0x0000);

    /* Panel Control -----------------------------------------------------------*/
    s_hal_extbrd_keil_mcbqvga_reg_write(0x90, 0x0010);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x92, 0x0000);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x93, 0x0003);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x95, 0x0110);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x97, 0x0000);
    s_hal_extbrd_keil_mcbqvga_reg_write(0x98, 0x0000);

    /* Set GRAM write direction
       I/D=10 (Horizontal : increment, Vertical : increment)
       AM=1   (address is updated in vertical writing direction)                */
    s_hal_extbrd_keil_mcbqvga_reg_write(0x03, 0x1038);

    s_hal_extbrd_keil_mcbqvga_reg_write(0x07, 0x0137);                 /* 262K color and display ON    */      
    
    
    
    memcpy(&info->cfg, cfg, sizeof(hal_extbrd_keil_mcbqvga_cfg_t));
    
    // store the i2caddress and the register address.
    return(hal_res_OK);
}

static uint16_t s_hal_extbrd_keil_mcbqvga_reg_read(uint8_t regaddr)
{
    s_hal_extbrd_keil_mcbqvga_cmd_write(regaddr);
    return(s_hal_extbrd_keil_mcbqvga_dat_read());
}

static void s_hal_extbrd_keil_mcbqvga_reg_write(uint8_t regaddr, uint16_t value)
{
    s_hal_extbrd_keil_mcbqvga_cmd_write(regaddr);
    s_hal_extbrd_keil_mcbqvga_dat_write(value);
}

static void s_hal_extbrd_keil_mcbqvga_cmd_write(uint8_t cmd)
{
    hal_spi_port_t spiport = s_hal_extbrd_keil_mcbqvga_info[0].cfg.spiport;
    
    hal_gpio_quickest_setval(gpio_cs.port, gpio_cs.pin, hal_gpio_valLOW);
  
  
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, HAL_EXTBRD_KEIL_MCBQVGA_SPI_START | HAL_EXTBRD_KEIL_MCBQVGA_SPI_WR | HAL_EXTBRD_KEIL_MCBQVGA_SPI_INDEX, NULL);
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, 0, NULL);
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, cmd, NULL);
    
    
    hal_gpio_quickest_setval(gpio_cs.port, gpio_cs.pin, hal_gpio_valHIGH);
}

static uint16_t s_hal_extbrd_keil_mcbqvga_dat_read(void)
{
    uint16_t value = 0;
    uint8_t vv0 = 0;
    uint8_t vv1 = 0;
 
    hal_spi_port_t spiport = s_hal_extbrd_keil_mcbqvga_info[0].cfg.spiport;
    
    hal_gpio_quickest_setval(gpio_cs.port, gpio_cs.pin, hal_gpio_valLOW);
 
 
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, HAL_EXTBRD_KEIL_MCBQVGA_SPI_START | HAL_EXTBRD_KEIL_MCBQVGA_SPI_RD | HAL_EXTBRD_KEIL_MCBQVGA_SPI_DATA, NULL);
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, 0, NULL);
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, 0, &vv1);
    value = vv1 << 8;
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, 0, &vv0);
    value |= vv0;
    
    
    
    
    hal_gpio_quickest_setval(gpio_cs.port, gpio_cs.pin, hal_gpio_valHIGH);
    
    return(value);
}

static void s_hal_extbrd_keil_mcbqvga_dat_write(uint16_t value)
{
    hal_spi_port_t spiport = s_hal_extbrd_keil_mcbqvga_info[0].cfg.spiport;
    
    hal_gpio_quickest_setval(gpio_cs.port, gpio_cs.pin, hal_gpio_valLOW);
 
 
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, HAL_EXTBRD_KEIL_MCBQVGA_SPI_START | HAL_EXTBRD_KEIL_MCBQVGA_SPI_WR | HAL_EXTBRD_KEIL_MCBQVGA_SPI_DATA, NULL);
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, value >> 8, NULL);
    s_hal_extbrd_keil_mcbqvga_spi_write(spiport, value & 0xff, NULL);
    
    hal_gpio_quickest_setval(gpio_cs.port, gpio_cs.pin, hal_gpio_valHIGH);
}

